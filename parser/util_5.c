#include"../minishell2.h"

int	replace_tab(char *str, char *dest, int *i, int *j)
{
	int	flag;

	flag = 0;
	if (str[*i] == '\t')
	{
		dest[*j] = ' ';
		(*j)++;
		(*i)++;
		flag = 1;
	}
	return (flag);
}
